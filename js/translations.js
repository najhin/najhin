// Current language
let currentLang = 'fr';

// Function to update page text
function updateLanguage(lang) {
  if (!translations[lang]) return;
  
  currentLang = lang;
  localStorage.setItem('najhin-lang', lang);
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[lang][key]) {
      const icon = element.querySelector('i');
      if (icon && element.childNodes.length > 1) {
        // Element has icon and text
        const iconHtml = icon.outerHTML;
        element.innerHTML = iconHtml + ' ' + translations[lang][key];
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });
  
  // Update placeholder for search input
  const searchInput = document.querySelector('.search-input');
  if (searchInput && translations[lang]['search.placeholder']) {
    searchInput.placeholder = translations[lang]['search.placeholder'];
  }
  
  // Update HTML direction for RTL (Arabic)
  if (lang === 'ar') {
    document.documentElement.setAttribute('dir', 'rtl');
    document.documentElement.setAttribute('lang', 'ar');
    document.body.style.direction = 'rtl';
    document.body.style.textAlign = 'right';
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.documentElement.setAttribute('lang', 'fr');
    document.body.style.direction = 'ltr';
    document.body.style.textAlign = 'left';
  }
  
  // Update active button state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    } else {
      btn.classList.remove('active');
    }
  });
  
  // Dispatch event for other components
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

// Get current language
function getCurrentLang() {
  return currentLang;
}

// Translate a specific key
function t(key) {
  return translations[currentLang][key] || translations.fr[key] || key;
}

// Initialize language switcher
function initLanguageSwitcher() {
  const savedLang = localStorage.getItem('najhin-lang') || 'fr';
  
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      const lang = btn.getAttribute('data-lang');
      updateLanguage(lang);
    });
  });
  
  updateLanguage(savedLang);
}

// Export for use
window.translations = translations;
window.updateLanguage = updateLanguage;
window.getCurrentLang = getCurrentLang;
window.t = t;
window.initLanguageSwitcher = initLanguageSwitcher;
